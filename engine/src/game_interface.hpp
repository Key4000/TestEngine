/**
 * @file game_interface.hpp
 * @brief Контракт взаимодействия между движком и пользовательской игрой (testbed).
 *
 * Этот файл определяет структуру `Game`, которую должна заполнить конкретная игра.
 * Движок использует эту структуру для получения настроек окна и указателей на игровые функции.
 */

#pragma once

#include <core/application.hpp>  // для ApplicationConfig

/**
 * @brief Основная структура, связывающая движок и игру.
 *
 * Каждая игра, использующая движок, должна предоставить заполненный экземпляр этой структуры
 * через функцию `create_game()`. Движок будет вызывать указанные функции в соответствующие моменты.
 */
struct Game {
    /**
     * @brief Указатель на внутреннее состояние игры.
     *
     * Движок не интерпретирует и не использует этот указатель напрямую. Он просто хранит его
     * и передаёт обратно в игровые функции (например, в `initialize`). Игра может использовать
     * его для хранения своих данных (например, указатель на класс GameState).
     */
    void* state;

    /**
     * @brief Конфигурация окна приложения.
     *
     * Игра заполняет это поле, чтобы задать начальные размеры, позицию и заголовок окна.
     * @see ApplicationConfig
     */
    ApplicationConfig app_config;

    /**
     * @brief Указатель на функцию инициализации игры.
     * @param game_inst Указатель на структуру `Game` (передаётся движком).
     * @return `true` при успешной инициализации, `false` при ошибке.
     */
    b8 (*initialize)(struct Game* game_inst);

    /**
     * @brief Указатель на функцию обновления логики игры.
     * @param game_inst Указатель на структуру `Game`.
     * @param delta_time Время, прошедшее с предыдущего кадра (в секундах).
     * @return `true` при успешном обновлении, `false` при ошибке (обычно приводит к остановке цикла).
     */
    b8 (*update)(struct Game* game_inst, f32 delta_time);

    /**
     * @brief Указатель на функцию рендера игры.
     * @param game_inst Указатель на структуру `Game`.
     * @param delta_time Время, прошедшее с предыдущего кадра.
     * @return `true` при успешном рендере, `false` при ошибке.
     */
    b8 (*render)(struct Game* game_inst, f32 delta_time);

    /**
     * @brief Указатель на функцию, вызываемую при изменении размера окна.
     * @param game_inst Указатель на структуру `Game`.
     * @param width Новая ширина окна (в пикселях).
     * @param height Новая высота окна.
     */
    void (*on_resize)(struct Game* game_inst, u32 width, u32 height);

    /**
     * @brief Указатель на функцию завершения работы игры.
     * @param game_inst Указатель на структуру `Game`.
     * @return `true` при успешном завершении, `false` при ошибке (движок всё равно завершит работу).
     */
    b8 (*shutdown)(Game* game_inst);
};

// Примечание: этот файл включается как в код движка, так и в код игры.
// Движок использует его, чтобы знать, какие функции вызывать.
// Игра использует его, чтобы знать, какую структуру нужно заполнить.