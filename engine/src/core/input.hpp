/**
 * @file input.hpp
 * @brief Система ввода (клавиатура, мышь). Низкоуровневый C-стиль, как в Kohi.
 *
 * Предоставляет доступ к текущему и предыдущему состоянию клавиш и кнопок мыши,
 * а также к координатам указателя. Все состояния хранятся в скрытых массивах
 * и обновляются один раз в кадр. Платформенный слой уведомляет систему о
 * событиях через функции обратного вызова (input_process_*).
 */

#pragma once

#include <defines.hpp>

//==============================================================================
// Коды клавиш (полный список, соответствующий виртуальным кодам Windows)
