/**
* @file defines.hpp
* @brief Глобальные определения типов, макросы экспорта/импорта, проверки платформы.
*/

#pragma once

//==============================================================================
// Настройки системы памяти
//==============================================================================

/**
* @def TE_MEMORY_STATS_ENABLED
* @brief Макрос, включающий сбор статистики памяти.
*
* Если определён, функции выделения/освобождения будут обновлять счётчики,
* и будет доступна функция `get_memory_usage_str()`. В релизной сборке
* обычно отключается.
*/
#define TE_MEMORY_STATS_ENABLED

//==============================================================================
// Целочисленные типы фиксированной ширины
//==============================================================================

typedef unsigned char u8; /**< 8-битное беззнаковое целое */
typedef unsigned short u16; /**< 16-битное беззнаковое целое */
typedef unsigned int u32; /**< 32-битное беззнаковое целое */
typedef unsigned long long u64; /**< 64-битное беззнаковое целое */

typedef signed char i8; /**< 8-битное знаковое целое */
typedef signed short i16; /**< 16-битное знаковое целое */
typedef signed int i32; /**< 32-битное знаковое целое */
typedef signed long long i64; /**< 64-битное знаковое целое */

//==============================================================================
// Типы с плавающей точкой
//==============================================================================

typedef float f32; /**< 32-битное число с плавающей точкой */
typedef double f64; /**< 64-битное число с плавающей точкой */

//==============================================================================
// Логические типы
//==============================================================================

typedef int b32; /**< 32-битный булев тип (используется для совместимости с WinAPI) */
typedef bool b8; /**< 8-битный булев тип (C++ bool) */

//==============================================================================
// Проверки размеров типов (статическая диагностика)
//==============================================================================

static_assert(sizeof(u8) == 1, "Expected u8 to be 1 byte.");
static_assert(sizeof(u16) == 2, "Expected u16 to be 2 bytes.");
static_assert(sizeof(u32) == 4, "Expected u32 to be 4 bytes.");
static_assert(sizeof(u64) == 8, "Expected u64 to be 8 bytes.");

static_assert(sizeof(i8) == 1, "Expected i8 to be 1 byte.");
static_assert(sizeof(i16) == 2, "Expected i16 to be 2 bytes.");
static_assert(sizeof(i32) == 4, "Expected i32 to be 4 bytes.");
static_assert(sizeof(i64) == 8, "Expected i64 to be 8 bytes.");

static_assert(sizeof(f32) == 4, "Expected f32 to be 4 bytes.");
static_assert(sizeof(f64) == 8, "Expected f64 to be 8 bytes.");

//==============================================================================
// Управление включением статистики памяти в релизе/дебаге
//==============================================================================

#ifndef TE_MEMORY_STATS_ENABLED
#ifdef NDEBUG
#define TE_MEMORY_STATS_ENABLED 0 ///< В релизе статистика отключена
#else
#define TE_MEMORY_STATS_ENABLED 1 ///< В дебаге статистика включена
#endif
#endif

//==============================================================================
// Экспорт/импорт символов для динамической библиотеки
//==============================================================================

/**
* @def TE_API
* @brief Макрос для экспорта/импорта символов при сборке DLL.
*
* При компиляции движка (когда определён `TE_EXPORT`) раскрывается в
* `__declspec(dllexport)`. При использовании движка из клиентского кода
* (когда `TE_EXPORT` не определён) раскрывается в `__declspec(dllimport)`.
* На не-Windows платформах макрос пустой.
*/
#if defined(_WIN32)
#ifdef TE_EXPORT
#define TE_API __declspec(dllexport)
#else
#define TE_API __declspec(dllimport)
#endif
#else
#define TE_API
#endif

//==============================================================================
// Определение платформы (только Windows 64-bit)
//==============================================================================

// Запрещаем 32-битную сборку → движок строго 64-bit
#if defined(WIN32) || defined(_WIN32) || defined(__WIN32__)
#define TE_PLATFORM_WINDOWS 1 /**< Макрос, указывающий на платформу Windows */
// Требуем 64-битную сборку
#ifndef _WIN64
#error "64-bit is required on Windows!"
#endif
#endif

// Запрещаем сборку на других платформах (временно)
#ifndef _WIN32
#error "Windows - единственная поддерживаемая платформа на данный момент."
#endif